var c = [21][41]int64{}

func cal(a, b int64) int64 {
	if a > b {
		return 0
	}
	if a == b || a == 0 {
		return 1
	}
	if c[a][b] > 0 {
		return c[a][b]
	}
	c[a][b] = cal(a-1, b-1) + cal(a, b-1)
	return c[a][b]
}

func main() {
	fmt.Println(cal(2, 4))
	fmt.Println(cal(20, 40))
}
