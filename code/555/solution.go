func SF(p, m int64) int64 {
	ans := int64(0)
	for k := int64(2); k <= p; k++ {
		for x := int64(1); x*x <= k; x++ {
			if k%x == 0 {
				ans += x * (((m-k)<<1 | 1) + x*3)
				if x != 1 && x*x != k {
					ans += (k / x) * (((m-k)<<1 | 1) + (k/x)*3)
				}
			}
		}
	}
	return ans / 2
}

